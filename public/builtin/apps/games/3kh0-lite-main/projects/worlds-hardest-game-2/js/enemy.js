function updateReverseCircularEnemy(n, l) {
	if(l==null)
		l = level;
	var e = enemies[l][n];
	if (e.enemyType == "reverseCircular") {
		if (e.stage == 0) {
			var newX = e.radius * Math.cos(e.angle * (Math.PI / 180));
		    var newY = e.radius * Math.sin(e.angle * (Math.PI / 180));
		    
		    e.x = newX + e.centerX;
		    e.y = newY + e.centerY;
		    
		    e.angle += e.speed;
		    
			e.simpleX = e.x / 40;
			e.simpleY = e.y / 40;
			
			if (e.angle >= e.endAngle) {
				e.stage = 1;
				e.angle = e.endAngle;
			}
		} else if (e.stage == 1) {
			var newX = e.radius * Math.cos(e.angle * (Math.PI / 180));
		    var newY = e.radius * Math.sin(e.angle * (Math.PI / 180));
		    
		    e.x = newX + e.centerX;
		    e.y = newY + e.centerY;
		    
		    e.angle -= e.speed;
		    
			e.simpleX = e.x / 40;
			e.simpleY = e.y / 40;
			
			if (e.angle <= e.startAngle) {
				e.stage = 0;
				e.angle = e.startAngle;
			}
		}
	}
}

